            setTimeout(() => {
              document.getElementsByClassName('lobby')[0].classList.add('lobby--end')
              this.show_messages()
            }, 2500)
          }, 300)
        }, 400)
      }
      else if (req.data.startsWith('lobby::new_user')) {
        let img_url = req.data.split('::').at(-2)
        let nickname = req.data.split('::').at(-1)
          document.getElementsByClassName('users_list')[0].classList.add('users_list--adding_elem')
