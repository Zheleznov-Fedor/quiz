      if (req.data === 'lobby::start_intro') {
        document.getElementsByClassName('lobby')[0].classList.add('lobby--active')
        setTimeout(() => {
          document.getElementsByClassName('hi')[0].classList.add('hi--active')
          setTimeout(() => {
